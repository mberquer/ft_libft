#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	int	*s2;

	s2 = (int *)s;
	i = 0;
	while (i < n)
		if (s2[i] == c)
			return (*s[i]);
	return (0);
}
